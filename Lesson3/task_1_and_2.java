public class main {
    public static void main(String[] args) {
        byte variable_1 = 10;
        byte variable_2 = 5;
        System.out.println("Первая переменная хранит значение: " + variable_1);
        System.out.println("Вторая переменная хранит значение: " + variable_2);
        System.out.println("Сумма переменных = " + (variable_1 + variable_2));

        System.out.println("variable_1 + variable_2 = " + variable_1 + " + " + variable_2 + " = " + (variable_1 + variable_2));
        System.out.println("variable_1 * variable_2 = " + variable_1 + " * " + variable_2 + " = " + (variable_1 * variable_2));
        System.out.println("variable_1 - variable_2 = " + variable_1 + " - " + variable_2 + " = " + (variable_1 - variable_2));
        System.out.println("variable_1 / variable_2 = " + variable_1 + " / " + variable_2 + " = " + (variable_1 / variable_2));

    }
}
